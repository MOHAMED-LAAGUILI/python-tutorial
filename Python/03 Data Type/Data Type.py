#----------------------------
# Type() => Shows type of data 
# Alm data in python are objects
#-------------------------------


print(type(10)) # int => Integer
print(type(-10)) # int
print(type(10.5)) # float 
print(type(-10.5)) # float 
print(type("Mohamed")) # String
print(type([1, 2, 3, 4, 5])) # List
print(type((1, 2, 3, 4, 5))) # Tuple
print(type({"One": 1, "Two": 2, "Three": 3}))# dict => Dictionary
print(type(2==2)) # bool => boolean
